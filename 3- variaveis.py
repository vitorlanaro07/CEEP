# Python é case-sensitive ou seja, se o texto escrito para declarar uma variável
# Estiver diferente, ela será uma nova váriavel
# exemplo:
Marca = "Volkswagen"

print(marca, Marca)

# mas, se você armazena um novo valor naquela variável, o valo antigo se perde
# exemplo
Marca = "meu antigo valor era: Volkswagen"
print(Marca)

